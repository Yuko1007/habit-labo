export function getTodayMessage(userCreatedAt: Date): string {
  const now = new Date();
  const daysSinceCreation = Math.floor(
    (now.getTime() - userCreatedAt.getTime()) / (24 * 60 * 60 * 1000)
  );

  let messageIndex: number;

  if (daysSinceCreation === 0) {
    // 初日: Day1
    messageIndex = 0;
  } else if (daysSinceCreation <= 29) {
    // Day2-30
    messageIndex = daysSinceCreation;
  } else {
    // Day31以降: Day2-31をループ
    messageIndex = ((daysSinceCreation - 1) % 30) + 1;
  }

  return dailyMessages[messageIndex];
}
